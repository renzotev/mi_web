            <p>El Ministerio de Trabajo y Promoción del Empleo generó 1,066 empleos temporales durante la actual gestión gubernamental en la región de Pasco con una inversión de más de S/.2,2 millones de nuevos soles. Así lo informó el titular del sector, Daniel  Maurate, quien precisó que, de esta forma, se benefició a 1561 participantes, quienes ejecutaron 27 proyectos de inversión pública.</p>
            <p>Estas metas alcanzadas en la región Pasco se dieron a conocer durante la reunión que sostuvo el ministro Maurate Romero con alcaldes provinciales y distritales, con el objetivo de promover la presentación de proyectos de inversión pública, intensivos en manos de obra no calificada, al programa ‘Trabaja Perú’.</p>
            <p>Cabe recordar que entre agosto de 2011 y marzo de 2015, el programa ha generado 123,964 empleos temporales beneficiando a 165,969 participantes, con una inversión de más de S/. 364.2 millones cofinanciados por este programa.</p>
            <p>Durante su estadía en Pasco, el ministro Maurate encabezó el lanzamiento de los aplicativos del Servicio de Orientación Vocacional e Información Ocupacional (SOVIO), en dos colegios de esa ciudad: el plantel 03 Antenor Rizo Patrón Lequerica y el colegio 34047 César Vallejo, ambos ubicados en el distrito de Yanacancha.</p>
            <p>Los aplicativos en donde pudieron participar los jóvenes fueron el ‘Foro Vocacional’, espacio virtual donde pueden formular temas de discusión, debates y consensos en torno a su trayectoria formativa laboral; y la prueba de intereses profesionales ‘Elige’, a través de la cual los beneficiarios podrán conocer a qué perfil vocacional pertenecen.</p>
            <p>Posteriormente, el titular del sector, Daniel Maurate, presentó el servicio del Certificado Único Laboral, el cual se otorga en el Centro de Empleo. Este documento está dirigido a los jóvenes entre los 18 y 29 años, así como a las personas con discapacidad y madres solteras, que se encuentran en búsqueda de trabajo.</p>
            <p>El Certificado Único Laboral permite obtener en breve tiempo y de forma gratuita, la identidad, la experiencia laboral formal y los antecedentes policiales de los ciudadanos. La entrega de este certificado se logró gracias a la suscripción de convenios entre el Ministerio del Interior, el Registro Nacional de Identidad y Estado Civil (RENIEC) y la Policía Nacional del Perú.</p>
            <p>En otro momento, el ministro Maurate hizo el anuncio sobre las 75 vacantes que ha puesto a disposición el programa ‘Jóvenes Productivos’, para que los beneficiarios puedan estudiar gratis los cursos de soldador, asistente en servicio de restaurante y ayudante de topografía. Dicha capacitación será brindada por el Instituto de Educación Superior Tecnológico Público Pasco.</p>
            <p>Maurate Romero indicó que este año se ha programado beneficiar en la región Pasco a más de 7 mil 900 jóvenes, a través del servicio de capacitación para la inserción laboral que será</p>
            <p>brindado por ‘Jóvenes Productivos’. Para ello, el sector invertirá una suma mayor de 41 mil 515 millones de nuevos soles.</p>
